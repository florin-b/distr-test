package com.distributie.beans;

public class Eveniment {
	private String eveniment;
	private String data;
	private String ora;
	private String distantaKM;

	public Eveniment() {

	}

	public String getEveniment() {
		return eveniment;
	}

	public void setEveniment(String eveniment) {
		this.eveniment = eveniment;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getOra() {
		return ora;
	}

	public void setOra(String ora) {
		this.ora = ora;
	}

	public String getDistantaKM() {
		return distantaKM;
	}

	public void setDistantaKM(String distantaKM) {
		this.distantaKM = distantaKM;
	}

}
